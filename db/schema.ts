/**
 * users — Base identity table (auto-generated by auth init)
 * Extended with additional roles for the Levav ecosystem.
 */
export const users = mysqlTable(
  "users",
  {
    id: serial("id").primaryKey(),
    unionId: varchar("union_id", { length: 255 }).unique(),
    name: varchar("name", { length: 255 }),
    email: varchar("email", { length: 320 }),
    avatar: text("avatar"),
    passwordHash: varchar("password_hash", { length: 255 }),
    loginType: mysqlEnum("login_type", ["oauth", "local"]).default("oauth").notNull(),
    emailVerified: boolean("email_verified").default(false).notNull(),
    // Roles: admin (system), talent (job seeker), employer (B2B buyer), creator (content producer)
    role: mysqlEnum("role", ["user", "admin", "talent", "employer", "creator"])
      .default("user")
      .notNull(),
    // Account status for operational gating
    accountStatus: mysqlEnum("account_status", ["active", "suspended", "pending_verification"])
      .default("active")
      .notNull(),
    createdAt: timestamp("created_at").defaultNow().notNull(),
    updatedAt: timestamp("updated_at")
      .defaultNow()
      .notNull()
      .$onUpdate(() => new Date()),
    lastSignInAt: timestamp("last_sign_in_at").defaultNow().notNull(),
  },
  (table) => ({
    unionIdIdx: uniqueIndex("users_union_id_idx").on(table.unionId),
    roleIdx: index("users_role_idx").on(table.role),
    emailIdx: index("users_email_idx").on(table.email),
  })
);

export type User = typeof users.$inferSelect;
export type InsertUser = typeof users.$inferInsert;

