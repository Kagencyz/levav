/**
 * wri_analytics — Workforce Readiness Index master record.
 * Stores the current computed WRI score and all 7 component scores.
 * Updated by backend triggers after significant profile events.
 */
export const wriAnalytics = mysqlTable(
  "wri_analytics",
  {
    id: serial("id").primaryKey(),
    profileId: bigint("profile_id", { mode: "number", unsigned: true })
      .notNull()
      .unique(), // 1:1 with levav_profiles

    // The global WRI score (0.00 - 100.00)
    wriScore: decimal("wri_score", { precision: 5, scale: 2 }).notNull().default("0.00"),
    // Gold Key badge tier derived from wri_score
    goldKeyTier: mysqlEnum("gold_key_tier", [
      "bronze",   // 0-39
      "silver",   // 40-59
      "gold",     // 60-74
      "platinum", // 75-89
      "diamond",  // 90-100
    ])
      .default("bronze")
      .notNull(),

    // The 7 component scores (each 0.00 - 100.00)
    cultureScore: decimal("culture_score", { precision: 5, scale: 2 }).notNull().default("0.00"),
    criticalThinkingScore: decimal("critical_thinking_score", { precision: 5, scale: 2 })
      .notNull()
      .default("0.00"),
    reliabilityScore: decimal("reliability_score", { precision: 5, scale: 2 })
      .notNull()
      .default("0.00"),
    communicationScore: decimal("communication_score", { precision: 5, scale: 2 })
      .notNull()
      .default("0.00"),
    learningScore: decimal("learning_score", { precision: 5, scale: 2 }).notNull().default("0.00"),
    leadershipScore: decimal("leadership_score", { precision: 5, scale: 2 })
      .notNull()
      .default("0.00"),
    impactScore: decimal("impact_score", { precision: 5, scale: 2 }).notNull().default("0.00"),

    // Calculation metadata
    calculationVersion: int("calculation_version").default(1).notNull(),
    lastCalculatedAt: timestamp("last_calculated_at").defaultNow().notNull(),
    createdAt: timestamp("created_at").defaultNow().notNull(),
    updatedAt: timestamp("updated_at")
      .defaultNow()
      .notNull()
      .$onUpdate(() => new Date()),
  },
  (table) => ({
    profileIdIdx: uniqueIndex("wri_profile_id_idx").on(table.profileId),
    wriScoreIdx: index("wri_score_idx").on(table.wriScore),
    goldKeyIdx: index("wri_gold_key_idx").on(table.goldKeyTier),
  })
);

export type WriAnalytics = typeof wriAnalytics.$inferSelect;
export type InsertWriAnalytics = typeof wriAnalytics.$inferInsert;

/**
 * wri_component_scores — Historical record of individual component score changes.
 * Every WRI recalculation inserts a row here for audit trail and trend analysis.
 */
export const wriComponentScores = mysqlTable(
  "wri_component_scores",
  {
    id: serial("id").primaryKey(),
    profileId: bigint("profile_id", { mode: "number", unsigned: true }).notNull(),
    // Which WRI component was updated
    componentType: mysqlEnum("component_type", [
      "culture",
      "critical_thinking",
      "reliability",
      "communication",
      "learning",
      "leadership",
      "impact",
    ]).notNull(),
    // The new score value
    score: decimal("score", { precision: 5, scale: 2 }).notNull(),
    // Weight applied in the global WRI calculation (0.00 - 1.00)
    weight: decimal("weight", { precision: 3, scale: 2 }).notNull().default("0.14"),
    // Source of the score change (for traceability)
    sourceEvent: mysqlEnum("source_event", [
      "levav_28_session",
      "volunteer_validation",
      "course_completion",
      "quickwork_shift",
      "employer_review",
      "skill_verification",
      "admin_override",
      "manual_update",
    ]).notNull(),
    // Reference to the source entity (e.g. session_id, shift_id, course_id)
    sourceEntityId: bigint("source_entity_id", { mode: "number", unsigned: true }),
    // Narrative context for the score change
    sourceDescription: varchar("source_description", { length: 500 }),
    recordedAt: timestamp("recorded_at").defaultNow().notNull(),
  },
  (table) => ({
    profileIdIdx: index("wri_comp_profile_id_idx").on(table.profileId),
    componentTypeIdx: index("wri_comp_component_type_idx").on(table.componentType),
    sourceEventIdx: index("wri_comp_source_event_idx").on(table.sourceEvent),
    recordedAtIdx: index("wri_comp_recorded_at_idx").on(table.recordedAt),
  })
);

export type WriComponentScore = typeof wriComponentScores.$inferSelect;
export type InsertWriComponentScore = typeof wriComponentScores.$inferInsert;

